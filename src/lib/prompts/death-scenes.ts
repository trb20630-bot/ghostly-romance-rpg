/**
 * 死亡劇情生成 Prompt
 * 根據玩家的現代身份設計死亡方式
 */

export function buildDeathScenePrompt(
  age: number,
  gender: string,
  occupation: string
): string {
  const genderText =
    gender === "male" ? "男性" : gender === "female" ? "女性" : "非二元性別";

  return `## 任務：設計現代死亡劇情

你需要為以下角色設計一段引人入勝的死亡劇情：

- 年齡：${age} 歲
- 性別：${genderText}
- 前世身份完整描述：${occupation}

**重要**：仔細閱讀上方的完整身份描述，死亡劇情必須體現其中提到的技能、背景和特質。如果描述中提到多項能力，至少要在劇情中暗示或展現其中幾項。

### 規則
1. 死亡方式必須符合該身份描述的日常情境和背景（利用描述中的細節設計）
2. 必須在 **12 句對話內** 完成死亡（從第一句描述到死亡發生）
3. 劇情要有戲劇性和吸引力，不能太平淡
4. 死亡過程要有張力但不過度血腥
5. 死亡前要有一個「命運的暗示」（如看到一隻白蝴蝶、聽到古琴聲等）
6. 死亡後描述靈魂離體的感受，為輪迴轉生做鋪墊

### 死亡設計參考
| 職業 | 可能的死亡方式 |
|------|---------------|
| 會計師 | 發現公司洗錢被滅口 |
| 記者 | 調查黑幕被追殺 |
| 護理師 | 醫療糾紛或過勞猝死 |
| 工程師 | 工安意外或爆肝猝死 |
| 老師 | 捲入校園醜聞被報復 |
| 業務 | 商業間諜被發現滅口 |

### 格式
用沉浸式第二人稱敘事。開始描述玩家的日常，逐漸引入不祥的徵兆，最後以意外或事件導致死亡。
結尾以靈魂離體、意識模糊作為轉場，為下一階段（輪迴）做準備。

開始吧。注意：這是故事的序章，要讓玩家立刻沉浸其中。`;
}

/**
 * 輪迴轉生過場 Prompt
 */
export function buildReincarnationPrompt(
  targetCharacter: "聶小倩" | "寧采臣"
): string {
  const characterDesc =
    targetCharacter === "聶小倩"
      ? "一個身著白衣的年輕女子，漂浮在月光下——你意識到，那就是你。你的雙腳懸在地面三寸之上。你已不再是人。你是鬼。"
      : "一個身穿青衫的書生，正坐在一間簡陋的客棧裡——你意識到，那就是你。桌上攤著幾本書卷，窗外是一片陌生的古代市鎮。你是寧采臣。";

  return `## 任務：描述輪迴轉生

玩家剛剛死亡，現在正在經歷輪迴轉生為「${targetCharacter}」。

### 描述要求
1. 先描述一段混沌朦朧的輪迴通道體驗（前世記憶如碎片閃過、遠方的梵唱、時空扭曲）
2. 然後玩家意識逐漸清醒，發現自己變成了另一個人
3. 最終呈現：${characterDesc}
4. 保留玩家的現代記憶——他們知道自己是「轉生」的
5. 以玩家對新身份的震驚和困惑結尾，給出第一個選項

### 寫作風格
- 輪迴通道部分用詩意、夢幻的筆觸
- 醒來部分用感官描寫（觸覺、嗅覺、視覺）
- 結尾給出 2-3 個選項讓玩家決定如何應對

### 格式要求（系統偵測用，必須遵守）
轉生敘事的結尾必須包含以下任一句式：
- 「你就是${targetCharacter}」
- 「你已轉生為${targetCharacter}」
- 「你的新身分是${targetCharacter}」`;
}
