export const HAIKU_PATTERNS = [
  /查看.*(背包|狀態|技能|地圖|物品|身上)/,
  /我有(什麼|哪些|多少)/,
  /現在(幾點|什麼時辰|白天|晚上)/,
  /我在哪/,
  /(在哪|位置|地點).*在哪/,
  /^(查看|檢查|確認)(一下)?$/,
  /時間/,
];

/**
 * 內部任務也用 Haiku（摘要、提取事實）
 */
export const INTERNAL_HAIKU_TASKS = ["summary", "extract_facts"] as const;

/**
 * 判斷是否應該用 Haiku 處理
 */
export function shouldUseHaiku(userMessage: string): boolean {
  return HAIKU_PATTERNS.some((pattern) => pattern.test(userMessage));
}
